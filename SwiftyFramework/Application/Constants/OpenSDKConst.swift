import UIKit

struct OpenSDKConst {
    
    // MARK: - 微信
    struct WeChat {
        static let id: String = ""
        static let secret: String = ""
    }
    
    // MARK: - 腾讯
    struct Tencent {
        static let id: String = ""
    }
    
    // MARK: - 新浪微博
    struct Sina {
        static let key: String = ""
        static let secret: String = ""
        static var redirectURI: String = ""
    }
    
    // MARK: - 个推
    struct Push {
        static let id: String = ""
        static let key: String = ""
        static let secret: String = ""
    }
    
}
